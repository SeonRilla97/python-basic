# 3. 논리 연산자 ( True / False )
# ------------------------------
age = 15
height = 120

# 나이가 10살 이상이고 키가 130이상인가?
print(f"놀이기구 탑승 가능 여부: {age >= 10 and height >= 130}")

# 나이가 10살 이상이거나 키가 130이상인가?
print(f"놀이기구 탑승 가능 여부: {age >= 10 or height >= 130}")

# 결과를 반대로 뒤집음
is_adult = True
print(f"성인 여부: {not is_adult}") # True