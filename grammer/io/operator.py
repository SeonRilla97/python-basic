# 2. 비교 연산자 ( True / False )
# ------------------------------

# 크기 비교
print(f"크다(>): 5 > 2는 {5>2}이다")
print(f"작다(<): 5 < 2는 {5<2}이다")
print(f"크거나 같다(>=): 5 >= 2는 {5>=2}이다")
print(f"작거나 같다(<=): 5 <= 2는 {5<=2}이다")

# 같다/다르다 (같다 ==)
pwd = "1234"
print(f"같다(==): 5 == 2는 {pw == '1234'}이다")
print(f"같지않다(!=): 5 != 2는 {pw != '1234'}이다")

print ("-" * 30)

